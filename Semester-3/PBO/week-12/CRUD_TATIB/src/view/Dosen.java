    private void tfNipKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNipKeyReleased
        int RowCount = tableDosen.getModel().getRowCount();
        for (int RecNum = 0; RecNum < RowCount; RecNum++) {
            if (tfNip.getText().equals(tableDosen.getValueAt(RecNum, 0).toString())) {
                FillForm(RecNum);
                break;
            } else
                EmptyForm();
        }
    }//GEN-LAST:event_tfNipKeyReleased


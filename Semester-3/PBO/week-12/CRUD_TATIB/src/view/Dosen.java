    private void EmptyForm() {
        int Year = java.time.LocalDate.now().getYear();
        int Month = java.time.LocalDate.now().getMonthValue();
        int Day = java.time.LocalDate.now().getDayOfMonth();
        Calendar calendar = new GregorianCalendar(Year, Month - 1, Day);
        // ----------------------------------------------------------
        tfNama.setText(null);
        tfTmpLahir.setText(null);
        spTglLahir.setValue(calendar.getTime());
        cbGender.setSelectedIndex(0);
        tfAlamat.setText(null);
        tfKabupatenKota.setText(null);
        tfProvinsi.setText(null);
    }

    private void FillForm(int RecNum) {
        tfNip.setText(tableDosen.getModel().getValueAt(RecNum, 0).toString());
        tfNama.setText(tableDosen.getModel().getValueAt(RecNum, 1).toString());
        tfTmpLahir.setText(tableDosen.getModel().getValueAt(RecNum, 2).toString());
        // ----------------------------------------------------------------------
        String Date = tableDosen.getModel().getValueAt(RecNum, 3).toString();
        int Year = Integer.parseInt(Date.substring(0, 4));
        int Month = Integer.parseInt(Date.substring(5, 7));
        int Day = Integer.parseInt(Date.substring(8, 10));
        Calendar calendar = new GregorianCalendar(Year, Month - 1, Day);
        spTglLahir.setValue(calendar.getTime());
        // ----------------------------------------------------------------------
        cbGender.setSelectedItem(tableDosen.getModel().getValueAt(RecNum, 4).toString());
        tfAlamat.setText(tableDosen.getModel().getValueAt(RecNum, 5).toString());       
        tfKabupatenKota.setText(tableDosen.getModel().getValueAt(RecNum, 6).toString());
        tfProvinsi.setText(tableDosen.getModel().getValueAt(RecNum, 7).toString());
    }


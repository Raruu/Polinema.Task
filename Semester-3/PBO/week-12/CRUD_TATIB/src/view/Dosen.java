    public String getspTglLahir() {
        String TglLahir = spTglLahir.getValue().toString();
        String Year = TglLahir.substring(24);
        String Month = TglLahir.substring(4, 7);
        switch (Month) {
            case "Jan" -> Month = "01";
            case "Feb" -> Month = "02";
            case "Mar" -> Month = "03";
            case "Apr" -> Month = "04";
            case "May" -> Month = "04";
            case "Jun" -> Month = "06";
            case "Jul" -> Month = "07";
            case "Aug" -> Month = "08";
            case "Sep" -> Month = "09";
            case "Oct" -> Month = "10";
            case "Nov" -> Month = "11";
            case "Dec" -> Month = "12";
        }
        String Day = TglLahir.substring(8, 10);
        return Year + "-" + Month + "-" + Day;
    }

